package com.codeo.variables;

public class DefaultVariables {

	public static void main(String[] args) {
	//integer value==>by default 0
		int a=10;
		System.out.println(a);
		//double by default value is 0.0
		double d=0.5;
		System.out.println(d);
		//string by default value is null
		String str="myfirstdemo";
		System.out.println(str);
		//boolean by default value is false
		boolean b=false;
		System.out.println(b);
		char c='n';
		System.out.println();
		

	}

}
